public class Plataform {
    private int y;
    private int x1;
    private int x2;

    public Plataform(int y,int x1,int x2){
        this.y = y;
        this.x1 = x1;
        this.x2 = x2;
    }
    public int getY(){
        return y;
    }
    public int getX1(){
        return x1;
    }
    public int getX2(){
        return x2;
    }

}
